// Return a shuffled copy of the array
// https://stackoverflow.com/a/6274381
const shuffle = (array) => {
  let a = array.slice();
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

export default shuffle;